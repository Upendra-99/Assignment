var a = 4;
var b = 2;

// Addition : 
console.log(a + b);

// Subtraction : 
console.log(a - b);

// Multiplication : 
console.log(a * b);

// Dicision : 
console.log(a / b);

// Remainder : 
console.log(a % b);

// Increasing the variable a: 
console.log(++a);

// Decreasing the variable b : 
console.log(--b);

// Diffrence between variable a raise to the power b and b raise to the power a  : 
console.log(a ** b - b ** a);